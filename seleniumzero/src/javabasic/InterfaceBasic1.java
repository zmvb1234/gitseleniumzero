/**
 * 接口不是类，接口只是声明了方法，没有具体实现。
 * 在JAVA编程语言中是一个抽象类型，是抽象方法的集合，接口通常以interface来声明。
 * 一个类通过继承接口的方式，从而来继承接口的抽象方法。
 * 接口无法被实例化，但是可以被实现。
 * 一个实现接口的类，必须实现接口内所描述的所有方法，否则就必须声明为抽象类。
 *
 */
